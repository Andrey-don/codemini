from bot.utils.openrouter import call_agent
from codemini.bot.utils.file_loader import read_project_file

MODEL = "anthropic/claude-haiku-4-5"
TEMPERATURE = 0.4

SYSTEM_PROMPT = """Ты — ресёрчер для сайта об онлайн-школах программирования для детей codemini.ru.

По заданной теме собираешь:
- Ключевые факты об онлайн-школах (возраст детей, стоимость, что входит в курс)
- Реальные отзывы родителей (положительные и критические)
- Сравнительные данные: цены, форматы занятий, результаты
- Чем школа выделяется среди конкурентов
- Типичные вопросы родителей по этой теме

Аудитория — родители 30-45 лет, выбирающие курс программирования для ребёнка.
Пиши только то, что реально поможет написать полезную и честную статью."""


def run(topic: str) -> str:
    brief = read_project_file("brief.md")
    context = f"""
ТЕМА: {topic}

САЙТ И АУДИТОРИЯ:
{brief}

Собери материал для статьи: факты о школах, цены, отзывы родителей, сравнения, типичные вопросы.
"""
    return call_agent(SYSTEM_PROMPT, context, MODEL, TEMPERATURE)
