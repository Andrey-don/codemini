from bot.utils.openrouter import call_agent

MODEL = "anthropic/claude-sonnet-4-6"
TEMPERATURE = 0.5

SYSTEM_PROMPT = """Ты — редактор сайта об онлайн-школах программирования для детей codemini.ru.

Правишь статью для родителей. Проверяй:
- Конкретность: везде ли указаны цены, возраст, условия?
- Честность: нет ли рекламных штампов ("лучшая", "уникальная")?
- Читаемость: не слишком ли длинные абзацы?
- Структура: есть ли чёткий лид и вывод?
- Польза: ответит ли статья на вопрос родителя?

НЕ меняй факты и цифры. НЕ добавляй то, чего нет в оригинале.
Возвращай только исправленный текст статьи."""

SYSTEM_PROMPT_SEO = """Ты — редактор seo-версии статьи для codemini.ru.

Применяй замечания SEO-агента к тексту статьи.
Исправляй только то, что реально улучшит SEO: заголовок H1, плотность ключевых слов, структуру.
НЕ меняй факты. НЕ добавляй рекламу.
Возвращай только исправленный текст статьи."""


def run(article: str) -> str:
    return call_agent(SYSTEM_PROMPT, article, MODEL, TEMPERATURE)


def run_seo_revision(article: str, seo_notes: str) -> str:
    context = f"""
СТАТЬЯ:
{article}

SEO-ЗАМЕЧАНИЯ:
{seo_notes}

Примени замечания и верни исправленную статью.
"""
    return call_agent(SYSTEM_PROMPT_SEO, context, MODEL, TEMPERATURE)
